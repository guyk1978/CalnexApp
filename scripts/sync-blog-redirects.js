/**
 * Regenerate safe trailing-slash 301 rules for blog posts in _redirects.
 * Never writes 200 → index.html rewrites (Cloudflare Pretty URL loops).
 */
const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const BLOG_JSON = path.join(ROOT, "data", "blog.json");
const REDIRECTS = path.join(ROOT, "_redirects");
const START = "# BLOG_TRAILING_SLASH_START";
const END = "# BLOG_TRAILING_SLASH_END";
const SKIP = new Set(["latest"]);

function loadSlugs() {
  const posts = JSON.parse(fs.readFileSync(BLOG_JSON, "utf8"));
  return posts
    .map((p) => (p && p.slug ? String(p.slug).trim().toLowerCase() : ""))
    .filter((s) => s && !SKIP.has(s))
    .sort();
}

function buildBlock(slugs) {
  const lines = [
    START,
    "# Auto-generated by: npm run redirects:blog (301 only — no 200 rewrites)",
    ...slugs.map((s) => `/blog/${s} /blog/${s}/ 301`),
    END
  ];
  return lines.join("\n");
}

function run() {
  const slugs = loadSlugs();
  const block = buildBlock(slugs);
  let text = fs.readFileSync(REDIRECTS, "utf8");

  if (!text.includes(START) || !text.includes(END)) {
    console.error(`sync-blog-redirects: missing ${START} / ${END} markers in _redirects`);
    process.exit(1);
  }

  const re = new RegExp(`${START}[\\s\\S]*?${END}`, "m");
  text = text.replace(re, block);
  fs.writeFileSync(REDIRECTS, text.endsWith("\n") ? text : `${text}\n`, "utf8");
  console.log(`sync-blog-redirects: wrote ${slugs.length} trailing-slash 301 rules`);
}

run();
