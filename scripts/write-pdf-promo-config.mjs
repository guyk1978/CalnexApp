/**
 * Write pdf-joinmypdf-promo.config.js for static export (prebuild).
 * Also mirrored to assets/js via sync-public-assets.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { ensureDir, PUBLIC_ASSETS, ROOT_ASSETS } from "./lib/ensure-public-assets.mjs";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const jsonPath = path.join(ROOT, "data", "pdf-joinmypdf-promo.json");

const body = `/** Auto-generated by write-pdf-promo-config.mjs — edit data/pdf-joinmypdf-promo.json */\nwindow.CalnexPdfJoinMyPdfPromo = ${JSON.stringify(
  JSON.parse(fs.readFileSync(jsonPath, "utf8")),
  null,
  2
)};\n`;

for (const base of [PUBLIC_ASSETS, ROOT_ASSETS]) {
  const outPath = path.join(base, "js", "pdf-joinmypdf-promo.config.js");
  ensureDir(path.dirname(outPath));
  fs.writeFileSync(outPath, body, "utf8");
}

console.log("write-pdf-promo-config: public/assets/js + assets/js");
