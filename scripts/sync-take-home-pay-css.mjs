/**
 * Mirror Take-Home Pay Next CSS module into public/assets/css (committed, always deployed).
 * Run after sync-next-to-assets.mjs in postbuild.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const DEST = path.join(ROOT, "public", "assets", "css", "take-home-pay-calculator.css");
const MARKER = ".take-home-pay_layout__";

const CSS_SOURCES = [
  path.join(ROOT, "out", "assets", "next", "static", "css"),
  path.join(ROOT, "public", "assets", "next", "static", "css"),
  path.join(ROOT, "assets", "next", "static", "css"),
];

function findTakeHomePayCssFile(dir) {
  if (!fs.existsSync(dir)) return null;
  for (const name of fs.readdirSync(dir)) {
    if (!name.endsWith(".css")) continue;
    const full = path.join(dir, name);
    const body = fs.readFileSync(full, "utf8");
    if (body.includes(MARKER)) return { full, body, name };
  }
  return null;
}

let match = null;
for (const dir of CSS_SOURCES) {
  match = findTakeHomePayCssFile(dir);
  if (match) break;
}

if (!match) {
  console.warn("sync-take-home-pay-css: no take-home-pay CSS chunk found — keeping existing file");
  process.exit(0);
}

const header = `/**
 * Take-Home Pay Calculator — stable bundle (mirrored from Next CSS module).
 * Served via style.css so the tool stays styled when /assets/next/static/css/*.css
 * is missing on the host (Cloudflare SPA fallback).
 * Regenerated by: node scripts/sync-take-home-pay-css.mjs
 * Source: ${match.name}
 */
`;

const payload = `${header}${match.body.trim()}\n`;

fs.writeFileSync(DEST, payload, "utf8");
console.log(`sync-take-home-pay-css: ${path.relative(ROOT, DEST)} ← ${match.name}`);

for (const mirror of [
  path.join(ROOT, "assets", "css", "take-home-pay-calculator.css"),
  path.join(ROOT, "out", "assets", "css", "take-home-pay-calculator.css"),
]) {
  fs.mkdirSync(path.dirname(mirror), { recursive: true });
  fs.writeFileSync(mirror, payload, "utf8");
  console.log(`sync-take-home-pay-css: mirrored → ${path.relative(ROOT, mirror)}`);
}
