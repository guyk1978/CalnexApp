/**
 * Mirror Take-Home Pay Next CSS into public/assets/css (exported via out/).
 * Cloudflare Pages rules: recursive mkdir, idempotent writes, never fail the build.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import {
  ensureDir,
  ensureOutAssets,
  ensurePublicAssets,
  PUBLIC_ASSETS,
  readUtf8IfExists,
  ROOT_ASSETS,
  writeFileIdempotent,
} from "./lib/ensure-public-assets.mjs";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const DEST = path.join(PUBLIC_ASSETS, "css", "take-home-pay-calculator.css");
const STYLE_CSS = path.join(PUBLIC_ASSETS, "css", "style.css");
const MARKER = ".take-home-pay_layout__";
const STYLE_IMPORT = '@import url("./take-home-pay-calculator.css");';

const CSS_SOURCES = [
  path.join(ROOT, "out", "assets", "next", "static", "css"),
  path.join(PUBLIC_ASSETS, "next", "static", "css"),
  path.join(ROOT_ASSETS, "next", "static", "css"),
];

const FALLBACK_SOURCES = [
  path.join(ROOT_ASSETS, "css", "take-home-pay-calculator.css"),
  path.join(ROOT, "assets", "css", "take-home-pay-calculator.css"),
  DEST,
];

const WRITE_TARGETS = [
  DEST,
  path.join(ROOT_ASSETS, "css", "take-home-pay-calculator.css"),
  path.join(ROOT, "assets", "css", "take-home-pay-calculator.css"),
  path.join(ROOT, "out", "assets", "css", "take-home-pay-calculator.css"),
];

/** mkdir -p every directory this script reads from or writes to. */
function ensureAllDirectories() {
  ensurePublicAssets();
  ensureOutAssets();
  ensureDir(path.join(PUBLIC_ASSETS, "css"));
  ensureDir(path.join(PUBLIC_ASSETS, "next", "static", "css"));
  ensureDir(path.join(ROOT_ASSETS, "css"));
  ensureDir(path.join(ROOT_ASSETS, "next", "static", "css"));
  ensureDir(path.join(ROOT, "assets", "css"));
  for (const dir of CSS_SOURCES) ensureDir(dir);
  for (const file of WRITE_TARGETS) ensureDir(path.dirname(file));
}

function findTakeHomePayCssFile(dir) {
  if (!fs.existsSync(dir)) return null;
  let entries;
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return null;
  }
  for (const ent of entries) {
    if (!ent.isFile() || !ent.name.endsWith(".css")) continue;
    const full = path.join(dir, ent.name);
    const body = readUtf8IfExists(full);
    if (body && body.includes(MARKER) && !body.trimStart().startsWith("<!")) {
      return { body, name: ent.name };
    }
  }
  return null;
}

function readFallbackBody() {
  for (const file of FALLBACK_SOURCES) {
    const body = readUtf8IfExists(file);
    if (body && body.includes(MARKER) && !body.trimStart().startsWith("<!")) {
      return { body, name: path.basename(file) };
    }
  }
  return null;
}

function buildPayload(body, sourceName) {
  const header = `/**
 * Take-Home Pay Calculator — stable bundle (mirrored from Next CSS module).
 * Served via style.css so the tool stays styled when /assets/next/static/css/*.css
 * is missing on the host (Cloudflare SPA fallback).
 * Regenerated by: node scripts/sync-take-home-pay-css.mjs
 * Source: ${sourceName}
 */
`;
  return `${header}${body.trim()}\n`;
}

/** Minimal valid bundle so downstream verify steps never break the pipeline. */
function buildPlaceholderPayload() {
  return buildPayload(
    `${MARKER}y5vCk{display:grid}`,
    "placeholder (re-run next build to regenerate)"
  );
}

function ensureStyleCssImport() {
  if (!fs.existsSync(STYLE_CSS)) return;
  try {
    let style = readUtf8IfExists(STYLE_CSS) || "";
    if (style.includes("take-home-pay-calculator.css")) return;
    const vibrant = '@import url("./vibrant-ui.css");';
    if (style.includes(vibrant)) {
      style = style.replace(vibrant, `${vibrant}\n${STYLE_IMPORT}`);
    } else {
      style = `${STYLE_IMPORT}\n${style}`;
    }
    writeFileIdempotent(STYLE_CSS, style);
  } catch (err) {
    console.warn("sync-take-home-pay-css: could not patch style.css import:", err.message);
  }
}

function resolvePayload() {
  for (const dir of CSS_SOURCES) {
    const match = findTakeHomePayCssFile(dir);
    if (match) return { payload: buildPayload(match.body, match.name), sourceName: match.name };
  }

  const fallback = readFallbackBody();
  if (fallback) {
    console.warn("sync-take-home-pay-css: Next CSS chunk not found — using committed fallback");
    return {
      payload: buildPayload(fallback.body, `${fallback.name} (fallback)`),
      sourceName: `${fallback.name} (fallback)`,
    };
  }

  console.warn("sync-take-home-pay-css: no source — writing placeholder (build continues)");
  return { payload: buildPlaceholderPayload(), sourceName: "placeholder" };
}

function writeAllTargets(payload, sourceName) {
  let wrote = 0;
  for (const target of WRITE_TARGETS) {
    try {
      ensureDir(path.dirname(target));
      if (writeFileIdempotent(target, payload)) {
        console.log(`sync-take-home-pay-css: ${path.relative(ROOT, target)} ← ${sourceName}`);
        wrote += 1;
      }
    } catch (err) {
      console.warn(`sync-take-home-pay-css: skip ${path.relative(ROOT, target)}:`, err.message);
    }
  }
  if (!wrote) console.log(`sync-take-home-pay-css: up to date (${sourceName})`);
}

function main() {
  ensureAllDirectories();
  ensureStyleCssImport();
  const { payload, sourceName } = resolvePayload();
  writeAllTargets(payload, sourceName);
}

try {
  main();
} catch (err) {
  console.warn("sync-take-home-pay-css: non-fatal error:", err.message);
  try {
    ensureAllDirectories();
    writeAllTargets(buildPlaceholderPayload(), "placeholder-after-error");
  } catch {
    /* last resort — still exit 0 */
  }
}

process.exit(0);
