/**
 * Write public/assets/js/consent-config.js from NEXT_PUBLIC_GA_ID / NEXT_PUBLIC_ADSENSE_ID.
 * Run in prebuild so Cloudflare env vars flow into static HTML pages.
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { ensureDir, PUBLIC_ASSETS, ROOT_ASSETS } from "./lib/ensure-public-assets.mjs";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const gaId = (process.env.NEXT_PUBLIC_GA_ID || "").trim();
const adsenseId = (process.env.NEXT_PUBLIC_ADSENSE_ID || "").trim();

const body = `/* Generated by scripts/write-consent-config.mjs — do not edit */
window.__CALNEX_CONSENT_CONFIG__ = ${JSON.stringify({ gaId, adsenseId }, null, 2)};
`;

const targets = [
  path.join(PUBLIC_ASSETS, "js", "consent-config.js"),
  path.join(ROOT_ASSETS, "js", "consent-config.js"),
];

for (const file of targets) {
  ensureDir(path.dirname(file));
  fs.writeFileSync(file, body, "utf8");
}

console.log(
  `write-consent-config: gaId=${gaId ? "[set]" : "[empty]"} adsenseId=${adsenseId ? "[set]" : "[empty]"}`
);
